#utils.py

def kg_to_lbs(kg):
    return kg * 2.20462

def lbs_to_kg(lbs):
    return lbs / 2.20462

def km_to_miles(km):
    return km * 0.621371

def miles_to_km(miles):
    return miles / 0.621371

def m_to_ft(m):
    return m * 3.28084

def ft_to_m(ft):
    return ft / 3.28084

def kmh_to_mph(kmh):
    return kmh * 0.621371

def mph_to_kmh(mph):
    return mph / 0.621371

def mps_to_kmh(mps):
    return mps * 3.6

def mps_to_mph(mps):
    return mps * 2.23694
